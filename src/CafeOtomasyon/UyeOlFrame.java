    private void KullanıcıTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_KullanıcıTextKeyTyped
        char giris = evt.getKeyChar();
        if (!(giris < '0' || giris > '9')) {
            evt.consume(); // kullanıcının girişi iptal olsun diye
            JOptionPane.showMessageDialog(this, "Sadece harf giriniz !", "Uyarı", 2); // 2=uyarı mesajı
        }
    }//GEN-LAST:event_KullanıcıTextKeyTyped

    private void KaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KaydetActionPerformed
        if (bosMu()) {
            String kullaniciadi = KullanıcıText.getText().trim();
            if (veri.adminVarMi(kullaniciadi)) {
                Admin admin = new Admin();
                admin.setId(veri.AdminTablosuMaxSatir());
                admin.setKullaniciadi(kullaniciadi);
                admin.setSifre(String.valueOf(SifreText.getPassword()));
                admin.setGuvenlikSorusu(jComboBox1.getSelectedItem().toString());
                admin.setCevap(Cevaptext.getText().trim());

                if (veri.ekle(admin)) {
                    JOptionPane.showMessageDialog(this, "Yönetici başarıyla eklendi.","Mesaj",1);
                    new GirisYapFrame().setVisible(true); // kayıt ol ekranına yönlendirdim
                    setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Yönetici eklenemedi!", "Uyarı", 2);
                }
            }else {
            JOptionPane.showMessageDialog(this, "Kullanıcı adı zaten kullanılıyor!", "Uyarı", 2);
        } 
        }
    }//GEN-LAST:event_KaydetActionPerformed
