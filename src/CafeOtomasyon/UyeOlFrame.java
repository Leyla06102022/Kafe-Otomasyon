    public boolean bosMu() {
        if (KullanıcıText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kullanıcı adı zorunludur.", "Uyarı", 2); // 2=uyarı mesajı
            return false;
        }
        if (String.valueOf(SifreText.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Şifre zorunludur.", "Uyarı", 2); // 2=uyarı mesajı
            return false;
        }
        if (jComboBox1.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Güvenlik sorusu zorunludur.", "Uyarı", 2); // 2=uyarı mesajı
            return false;
        }
        if (Cevaptext.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cevap zorunludur.", "Uyarı", 2); // 2=uyarı mesajı
            return false;
        }
        return true;
    }

